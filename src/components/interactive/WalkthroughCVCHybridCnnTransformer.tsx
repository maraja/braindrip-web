import { useState } from 'react';

const STEPS = [
    { title: '1. General Design Pattern', desc: 'A typical hybrid architecture follows this structure:  CNN stem or early stages: Convolutional layers process the raw image, producing feature maps at reduced spatial resolution (e.g., stride 16). Tokenization: The CNN feature map is flattened or projected into a sequence of tokens.' },
    { title: '2. CoAtNet: Systematic Hybridization', desc: 'CoAtNet (Dai et al., 2021, Google) systematically studies which combination of convolution (C) and attention (A) stages works best. The architecture uses 5 stages labeled S0-S4:  [equation]  Key design decisions:  Stages S0-S1: MBConv blocks (depthwise separable convolutions from EfficientNet).' },
    { title: '3. Other Notable Hybrids', desc: 'LeViT (Graham et al., 2021): Replaces the patch embedding with a convolutional stem of 4 convolution-BN-GELU blocks with stride 2, then uses a compact attention-based body. Designed for fast inference -- LeViT-256 achieves 81.6% top-1 at 1000+ images/second on a single GPU.' },
    { title: '4. Why CNNs Work Better Early', desc: 'The advantage of convolutional early layers has a principled explanation:  High resolution, local patterns: Early layers process high-resolution feature maps (56 x 56 or 112 x 112). Attention over thousands of tokens is wasteful when the relevant patterns are local (edges, corners).' },
];

